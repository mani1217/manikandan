package week1day3;

public class swappingafter {
public static void main(String []args) {
	int a=10;
	int b=20;
	System.out.println("after swapping");
	System.out.println("a="+a+ "b="+b);
	int temp=b;
	a=b;
	b=temp;
	System.out.println("after swapping");
	System.out.println("a="+a+ "b"+b);
}
}
